package com.ezen709.myhome.model;

public class DepartmentDTO {
	private int dep_Code;
	private String dep_Name;
	private String dep_Head;
	public int getDep_Code() {
		return dep_Code;
	}
	public void setDep_Code(int dep_Code) {
		this.dep_Code = dep_Code;
	}
	public String getDep_Name() {
		return dep_Name;
	}
	public void setDep_Name(String dep_Name) {
		this.dep_Name = dep_Name;
	}
	public String getDep_Head() {
		return dep_Head;
	}
	public void setDep_Head(String dep_Head) {
		this.dep_Head = dep_Head;
	}
	
	
	
	
}
