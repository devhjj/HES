package com.ezen709.myhome.model;

public class DoctorDTO {
	private int doc_Code;
	private String doc_Rank;
	private String doc_Major;
	private String resident_Num;
	private String doc_Name;
	private int dep_Code;
	
	public int getDoc_Code() {
		return doc_Code;
	}
	public void setDoc_Code(int doc_Code) {
		this.doc_Code = doc_Code;
	}
	public String getDoc_Rank() {
		return doc_Rank;
	}
	public void setDoc_Rank(String doc_Rank) {
		this.doc_Rank = doc_Rank;
	}
	public String getDoc_Major() {
		return doc_Major;
	}
	public void setDoc_Major(String doc_Major) {
		this.doc_Major = doc_Major;
	}
	public String getResident_Num() {
		return resident_Num;
	}
	public void setResident_Num(String resident_Num) {
		this.resident_Num = resident_Num;
	}
	public String getDoc_Name() {
		return doc_Name;
	}
	public void setDoc_Name(String doc_Name) {
		this.doc_Name = doc_Name;
	}
	public int getDep_Code() {
		return dep_Code;
	}
	public void setDep_Code(int dep_Code) {
		this.dep_Code = dep_Code;
	}
	
}
